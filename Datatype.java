public class Datatype {

  public static void main(String[] args) {
    String s = "Hello";
    int i = 3;
    double d = 2.2;
    char c = 'B';
    boolean b = true;

    System.out.println(s);
    System.out.println(i);
    System.out.println(d);
    System.out.println(c);
    System.out.println(b);
  }
}
